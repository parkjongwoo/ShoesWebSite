package pjw.deal.model;

public class DProduct {
	private int did;
	private int pid;
	private int dp_quantity;
	private int cid;
	public int getDid() {
		return did;
	}
	public void setDid(int did) {
		this.did = did;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getDp_quantity() {
		return dp_quantity;
	}
	public void setDp_quantity(int dp_quantity) {
		this.dp_quantity = dp_quantity;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	
}
