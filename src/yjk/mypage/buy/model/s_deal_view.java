package yjk.mypage.buy.model;

public class s_deal_view {
	private String ddate;
	private int pid;
	private int dp_quantity;
	private int ddcharge;
	private int dtotal;
	private String ddstate;
	private String pname;
	public String getDdate() {
		return ddate;
	}
	public void setDdate(String ddate) {
		this.ddate = ddate;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getDp_quantity() {
		return dp_quantity;
	}
	public void setDp_quantity(int dp_quantity) {
		this.dp_quantity = dp_quantity;
	}
	public int getDdcharge() {
		return ddcharge;
	}
	public void setDdcharge(int ddcharge) {
		this.ddcharge = ddcharge;
	}
	public int getDtotal() {
		return dtotal;
	}
	public void setDtotal(int dtotal) {
		this.dtotal = dtotal;
	}
	public String getDdstate() {
		return ddstate;
	}
	public void setDdstate(String ddstate) {
		this.ddstate = ddstate;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}	
}
